import math


def add(a: int, b: int) -> int:
    return a + b

def subtract(a: int, b: int) -> int:
    return a - b

def multiply(a: int, b: int) -> int:
    return a * b

def divide(a: int, b: int) -> int:
    return a / b

def double(a: int) -> int:
    return a * 2

def square(a: int) -> int:
    return a * a

def square_root(a: int) -> int:
    return math.sqrt(a)